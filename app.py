@st.cache_data(show_spinner="📂 Memuat dataset...")
def load_data() -> pd.DataFrame:
    """Memuat dataset dari CSV dan mengembalikan DataFrame mentah."""
    if not os.path.exists(DATA_PATH):
        st.error(f"❌ File dataset tidak ditemukan: `{DATA_PATH}`\n\nLetakkan `data.csv` di folder `data/`.")
        st.stop()
    return pd.read_csv(DATA_PATH)


@st.cache_data(show_spinner="⚙️ Memproses data...")
def preprocess_data(df: pd.DataFrame):
    """
    Preprocessing: drop kolom tak relevan, drop null, One-Hot Encoding,
    pisahkan X (fitur) dan y (target).
    """
    df = df.copy()
    cols_to_drop = [c for c in DROP_COLS if c in df.columns]
    df.drop(columns=cols_to_drop, inplace=True)
    df.dropna(inplace=True)

    y = df[TARGET].copy()
    X = df.drop(columns=[TARGET])

    cat_present = [c for c in CAT_COLS if c in X.columns]
    X = pd.get_dummies(X, columns=cat_present, drop_first=False)
    X = X.astype(float)

    return X, y, list(X.columns)


def save_model(model, path: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    joblib.dump(model, path)


def load_model(path: str):
    if os.path.exists(path):
        return joblib.load(path)
    return None


def save_ann(model, path: str = ANN_PATH) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    model.save(path)


def load_ann(path: str = ANN_PATH):
    if os.path.exists(path):
        try:
            import tensorflow as tf
            return tf.keras.models.load_model(path)
        except Exception:
            return None
    return None


def models_exist() -> bool:
    return all(os.path.exists(p) for p in [LR_PATH, RF_PATH, ANN_PATH, SCALER_PATH])


def compute_metrics(y_true, y_pred) -> dict:
    mae  = mean_absolute_error(y_true, y_pred)
    mse  = mean_squared_error(y_true, y_pred)
    rmse = np.sqrt(mse)
    r2   = r2_score(y_true, y_pred)
    return {"MAE": mae, "MSE": mse, "RMSE": rmse, "R²": r2}


def format_currency(value: float) -> str:
    return f"${value:,.0f}"


def price_category(price: float, y: pd.Series) -> str:
    q33 = y.quantile(0.33)
    q66 = y.quantile(0.66)
    if price < q33:
        return "low"
    elif price < q66:
        return "medium"
    return "high"


