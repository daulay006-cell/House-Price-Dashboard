def build_ann(input_dim: int):
    import tensorflow as tf
    model = tf.keras.Sequential([
        tf.keras.layers.Input(shape=(input_dim,)),
        tf.keras.layers.Dense(256, activation="relu"),
        tf.keras.layers.BatchNormalization(),
        tf.keras.layers.Dropout(0.3),
        tf.keras.layers.Dense(128, activation="relu"),
        tf.keras.layers.BatchNormalization(),
        tf.keras.layers.Dropout(0.2),
        tf.keras.layers.Dense(64, activation="relu"),
        tf.keras.layers.Dense(1),
    ])
    model.compile(optimizer=tf.keras.optimizers.Adam(learning_rate=0.001), loss="mse", metrics=["mae"])
    return model


def train_all_models(X_train, X_test, y_train, y_test, n_estimators: int = 200):
    os.makedirs(MODEL_DIR, exist_ok=True)
    results = {}

    with st.spinner("🏃 Training Linear Regression..."):
        t0 = time.time()
        lr = LinearRegression()
        lr.fit(X_train, y_train)
        train_time_lr = time.time() - t0

        t1 = time.time()
        y_pred_lr = lr.predict(X_test)
        pred_time_lr = time.time() - t1

        save_model(lr, LR_PATH)
        metrics_lr = compute_metrics(y_test, y_pred_lr)
        metrics_lr["Training Time (s)"] = round(train_time_lr, 4)
        metrics_lr["Prediction Time (s)"] = round(pred_time_lr, 6)
        results["Linear Regression"] = {"metrics": metrics_lr, "y_pred": y_pred_lr, "model": lr}

    with st.spinner(f"🌲 Training Random Forest ({n_estimators} trees)..."):
        t0 = time.time()
        rf = RandomForestRegressor(n_estimators=n_estimators, random_state=42, n_jobs=-1)
        rf.fit(X_train, y_train)
        train_time_rf = time.time() - t0

        t1 = time.time()
        y_pred_rf = rf.predict(X_test)
        pred_time_rf = time.time() - t1

        save_model(rf, RF_PATH)
        metrics_rf = compute_metrics(y_test, y_pred_rf)
        metrics_rf["Training Time (s)"] = round(train_time_rf, 2)
        metrics_rf["Prediction Time (s)"] = round(pred_time_rf, 6)
        results["Random Forest"] = {"metrics": metrics_rf, "y_pred": y_pred_rf, "model": rf}

    with st.spinner("🧠 Training Artificial Neural Network..."):
        import tensorflow as tf

        scaler = StandardScaler()
        X_train_sc = scaler.fit_transform(X_train)
        X_test_sc  = scaler.transform(X_test)
        save_model(scaler, SCALER_PATH)

        ann = build_ann(X_train_sc.shape[1])
        early_stop = tf.keras.callbacks.EarlyStopping(monitor="val_loss", patience=15, restore_best_weights=True)

        t0 = time.time()
        history = ann.fit(
            X_train_sc, y_train, validation_split=0.1, epochs=200, batch_size=64,
            callbacks=[early_stop], verbose=0,
        )
        train_time_ann = time.time() - t0

        t1 = time.time()
        y_pred_ann = ann.predict(X_test_sc, verbose=0).flatten()
        pred_time_ann = time.time() - t1

        save_ann(ann, ANN_PATH)
        metrics_ann = compute_metrics(y_test, y_pred_ann)
        metrics_ann["Training Time (s)"] = round(train_time_ann, 2)
        metrics_ann["Prediction Time (s)"] = round(pred_time_ann, 4)
        results["ANN"] = {
            "metrics": metrics_ann, "y_pred": y_pred_ann, "model": ann,
            "history": history, "X_test_sc": X_test_sc,
        }

    return results


